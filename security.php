<p><strong>Username and Password Security</strong></p>
<p>Access to&nbsp;<strong><em>ITU</em></strong>&nbsp;Internet Banking Service is based on a Username and password. This Username and password is chosen by you at the time of registration. You must keep your Username and Password confidential and take steps to prevent unauthorized use of them. You must not tell or disclose them to another person or allow them to be seen by another person (including family or friends). You must not keep a record of them in a way which they can be determined by another person. You must not record them together. You must not select a Password, which represents your birth date or a recognizable part of your name.</p>
