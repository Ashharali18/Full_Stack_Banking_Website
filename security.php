<p>Allied Bank uses a very high level of encryption to protect your transactions and Accounts from unauthorized access. This includes security in terms of hardware and software at our end such as Firewalls, SSL certificates and other security software.</p>
<p>All information that travels between your computer and&nbsp;<strong><em>ITU</em></strong>&nbsp;Internet Banking Server(s) is secured with 128-bit SSL encryption. This means information cannot be read or tempered between any points on Internet. This ensures privacy of your transactions and any other data that travels over Internet.</p>
